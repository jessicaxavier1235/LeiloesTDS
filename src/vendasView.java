    private void listarVendidos() {
    try {
        ProdutosDAO dao = new ProdutosDAO();

        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) tblVendas.getModel();

        model.setNumRows(0); // limpa tabela

        java.util.ArrayList<ProdutosDTO> lista = dao.listarProdutosVendidos();

        for (ProdutosDTO p : lista) {
            model.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getValor(),
                p.getStatus()
            });
        }

    } catch (Exception e) {
        System.out.println("Erro ao listar vendidos: " + e.getMessage());
    }
}
 
